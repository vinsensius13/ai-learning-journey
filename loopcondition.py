# If-Else
angka = int(input("Masukkan angka: "))
if angka % 2 == 0:
    print(f"{angka} adalah bilangan genap.")
else:
    print(f"{angka} adalah bilangan ganjil.")

# For Loop
for i in range(1, 6):
    print(f"Perulangan ke-{i}")

# While Loop
count = 0
while count < 5:
    print(f"Hitungan: {count}")
    count += 1
